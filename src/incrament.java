public class incrament {
    public static void main(String args[]){
        int gumballs = 27;                          //set var

        ++gumballs;                                 //incrament

        System.out.println(gumballs);               //print
        System.out.println(++gumballs);             //incrament again ... 
        System.out.println(++gumballs);

    }
}
